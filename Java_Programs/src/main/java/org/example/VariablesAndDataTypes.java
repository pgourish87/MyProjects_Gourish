package org.example;

public class VariablesAndDataTypes {
    public static void main(String[] args) {

        // primitive data types
        byte b = 10;
        short s = 20;
        int i = 1000;
        long l = 1000000000L;
        float f = 3.14f;
        double d = 3.14;
        char c = 'A';
        boolean bool = true;
        String str = "Gourish";

        //Wrapper class data types
        Byte b1 = 10;
        Short s1 = 20;
        Integer i1 = 1000;
        Long l1 = 1000000000L;
        Float f1 = 3.14f;
        Double d1 = 3.14;
        Character c1 = 'A';
        Boolean bool1 = true;
    }
}
